/**
 * Get privileges filtered by category
 * @param category - The privilege category to filter by
 * @returns Array of privileges in the specified category
 */
export function getPrivilegesByCategory(category: PrivilegeCategory): DentalPrivilege[] {
  return dentalPrivileges.filter((privilege) => privilege.category === category);
}

/**
 * Get a privilege by its ID
 * @param id - The privilege ID
 * @returns The privilege or undefined if not found
 */
export function getPrivilegeById(id: string): DentalPrivilege | undefined {
  return dentalPrivileges.find((privilege) => privilege.id === id);
}

/**
 * Get a privilege by its code
 * @param code - The privilege code (e.g., "CORE-001")
 * @returns The privilege or undefined if not found
 */
export function getPrivilegeByCode(code: string): DentalPrivilege | undefined {
  return dentalPrivileges.find((privilege) => privilege.code === code);
}

/**
 * Get all privileges that require special qualification
 * @returns Array of privileges requiring special qualification
 */
export function getPrivilegesRequiringQualification(): DentalPrivilege[] {
  return dentalPrivileges.filter((privilege) => privilege.requiresSpecialQualification);
}

/**
 * Search privileges by name (English or Arabic)
 * @param searchTerm - The search term
 * @returns Array of matching privileges
 */
export function searchPrivileges(searchTerm: string): DentalPrivilege[] {
  const lowerSearchTerm = searchTerm.toLowerCase();
  return dentalPrivileges.filter(
    (privilege) =>
      privilege.nameEn.toLowerCase().includes(lowerSearchTerm) ||
      privilege.nameAr.includes(searchTerm) ||
      (privilege.description && privilege.description.toLowerCase().includes(lowerSearchTerm))
  );
}

/**
 * Get count of privileges by category
 * @returns Object with category names as keys and counts as values
 */
export function getPrivilegeCountByCategory(): Record<PrivilegeCategory, number> {
  const counts: Record<PrivilegeCategory, number> = {
    [PrivilegeCategory.CORE]: 0,
    [PrivilegeCategory.RESTORATIVE]: 0,
    [PrivilegeCategory.PEDIATRIC]: 0,
    [PrivilegeCategory.ORTHODONTICS]: 0,
    [PrivilegeCategory.PROSTHODONTICS]: 0,
    [PrivilegeCategory.PERIODONTICS]: 0,
    [PrivilegeCategory.ORAL_SURGERY]: 0,
    [PrivilegeCategory.ENDODONTICS]: 0,
    [PrivilegeCategory.ORAL_MEDICINE]: 0,
    [PrivilegeCategory.RADIOLOGY]: 0,
  };

  dentalPrivileges.forEach((privilege) => {
    counts[privilege.category]++;
  });

  return counts;
}

